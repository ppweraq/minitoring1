#!/bin/bash

parametr="^[+,-]?[0-9]+([., ,][0-9]+)?$" 

if [[ $# -gt 1 ]]; then
        echo "Ошибка: больше одного параметра."
elif [[ $# -ne 1 ]]; then
        echo "Ошибка: не передан текстовый параметр"
fi
if [[ $# = 1 ]]; then
    if [[ $1 =~ $parametr ]]; then
        echo "Ошибка: некорректный ввод. Попробуйте текстовый параметр"
    else
        echo "Параметр: $1"
    fi
fi

